    void addDataOnline(){
        AndroidNetworking.get("https://www.thesportsdb.com/api/v1/json/1/eventsnextleague.php?id=4328")
                .setTag("test")
                .setPriority(Priority.LOW)
                .build()
                .getAsJSONObject(new JSONObjectRequestListener() {
                    @Override
                    public void onResponse(JSONObject response) {
                        // do anything with response
                        Log.d("hasiljson", "onResponse: " + response.toString());
                        //jika sudah berhasil debugm lanjutkan code dibawah ini
                        DataArrayListEvent = new ArrayList<>();
                        ModelEvent modelku;
                        try {
                            Log.d("hasiljson", "onResponse: " + response.toString());
                            JSONArray jsonArray = response.getJSONArray("events");
                            Log.d("hasiljson2", "onResponse: " + jsonArray.toString());
                            for (int i = 0; i < jsonArray.length(); i++) {
                                modelku = new ModelEvent();
                                JSONObject jsonObject = jsonArray.getJSONObject(i);
                                modelku.setStrHomeTeam(jsonObject.getString("strHomeTeam"));
                                modelku.setStrAwayTeam(jsonObject.getString("strAwayTeam"));
                                modelku.setDateEvent(jsonObject.getString("dateEvent"));
                                modelku.setStrTime(jsonObject.getString("strTime"));
                                DataArrayListEvent.add(modelku);
                            }
                            //untuk handle click
                            adapterEvent = new DataAdapterEvent(DataArrayListEvent, new DataAdapterEvent.Callback() {
                                @Override
                                public void onClick(int position) {

                                }

                                @Override
                                public void test() {

                                }
                            });
                            RecyclerView.LayoutManager layoutManager = new LinearLayoutManager(getActivity());
                            recyclerViewEvent.setLayoutManager(layoutManager);
                            recyclerViewEvent.setAdapter(adapterEvent);
                        } catch (JSONException e) {
                            e.printStackTrace();
                        }

                    }

                    @Override
                    public void onError(ANError error) {
                        // handle error
                        Log.d("errorku", "onError errorCode : " + error.getErrorCode());
                        Log.d("errorku", "onError errorBody : " + error.getErrorBody());
                        Log.d("errorku", "onError errorDetail : " + error.getErrorDetail());
                    }
                });
    }
